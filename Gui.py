    def run_chosen(self):
        if ex.checkBox_sd.isChecked():
            ser_sent = Main.openSerial_sent(ex)
        ser = Main.openSerial(ex)
        Main.guiInit(ex, True)
        global Device
        Device = Main.device_info(ex, ser)

        ex.label_MAC.setText(_translate("Form", Device[0], None))
        ex.label_SW.setText(_translate("Form", Device[1], None))
        # print ex.comboBox_platform.currentText()
        if ex.checkBox_reboot.isChecked():
            if Reboot.reboot(ser, ex):
                ex.label_reboot_result.setText(_translate("Form", 'PASS', None))
                ex.label_reboot_result.setStyleSheet("QLabel#label_reboot_result {color: green}")
            else:
                ex.label_reboot_result.setText(_translate("Form", 'FAIL', None))
                ex.label_reboot_result.setStyleSheet("QLabel#label_reboot_result {color: red}")
        if ex.checkBox_sd.isChecked():
            if SD.sd(ser, ser_sent, ex):
                ex.label_sd_result.setText(_translate("Form", 'PASS', None))
                ex.label_sd_result.setStyleSheet("QLabel#label_sd_result {color: green}")
            else:
                ex.label_sd_result.setText(_translate("Form", 'FAIL', None))
                ex.label_sd_result.setStyleSheet("QLabel#label_sd_result {color: red}")

        Main.guiInit(ex, False)

